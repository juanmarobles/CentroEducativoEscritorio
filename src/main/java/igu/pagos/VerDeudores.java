private void mostrarTablaDeudores() {
        // Obt√©n la lista de alumnos con cuotas impagas
        List<Alumno> alumnosConCuotasImpagas = obtenerAlumnosConCuotasImpagas();

        // Filas y columnas no editables
        DefaultTableModel tabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        // Nombres de columnas
        String titulos[] = {"id_Alumno", "Nombre y Apellido", "Tutor"};
        tabla.setColumnIdentifiers(titulos);

        // Recorrer la lista de alumnos con cuotas impagas y mostrar elementos en la tabla
        if (alumnosConCuotasImpagas != null) {
            for (Alumno a : alumnosConCuotasImpagas) {
                String nombreTutor = (a.getTutor() != null) ? a.getTutor().getNombre() + " " + a.getTutor().getApellido() : "Sin Tutor";
                Object[] objeto = {a.getId(), a.getNombre() +" "+a.getApellido(), nombreTutor};
                tabla.addRow(objeto);
            }
        }

        tablaDeudores.setModel(tabla);
    }

    private List<Alumno> obtenerAlumnosConCuotasImpagas() {
        List<Alumno> deudores = new ArrayList<>();

        // Obtener todas las cuotas desde la base de datos
        List<Cuota> cuotas = control.traerCuotas();

        // Recorrer las cuotas para encontrar los alumnos con cuotas impagas
        for (Cuota cuota : cuotas) {
            if (!cuota.getPagado()) {
                Alumno alumno = cuota.getAlumno();
                if (!deudores.contains(alumno)) {
                    deudores.add(alumno);
                }
            }
        }

        return deudores;
    }

