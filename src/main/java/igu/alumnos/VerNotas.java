    private void cargarAlumnos() {
        ModeloAlumno modAlumno = new ModeloAlumno();
        ArrayList<Alumno> listaAlumnos = modAlumno.getAlumnos();

        cmbAlumno.setEditable(false);

        // Limpiar el ComboBox
        cmbAlumno.removeAllItems();

        // Agregar todos los alumnos al ComboBox
        for (Alumno alumno : listaAlumnos) {
            cmbAlumno.addItem(alumno);
        }

        // Eliminar la opción en blanco después de configurar el decorador
        cmbAlumno.removeItem("");
        // Establecer el índice seleccionado a -1 para no mostrar ninguna selección
        cmbAlumno.setSelectedIndex(-1);

        // Agregar ActionListener para capturar el evento de selección de alumno
        cmbAlumno.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Alumno alumnoSeleccionado = (Alumno) cmbAlumno.getSelectedItem();
                if (alumnoSeleccionado != null) {
                    mostrarTablaAlumnos(alumnoSeleccionado);
                }
            }
        });
    }

    private void mostrarTablaAlumnos(Alumno alumnoSeleccionado) {
        List<Nota> listaNotas = control.traerNotasPorAlumno(alumnoSeleccionado);

        DefaultTableModel tabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        String titulos[] = {"Nombre", "Apellido", "Nota", "Materia"};
        tabla.setColumnIdentifiers(titulos);
        
        if (listaNotas != null) {
            for (Nota nota : listaNotas) {
                Object[] objeto = {nota.getAlumno().getNombre(), nota.getAlumno().getApellido(), nota.getValor(), nota.getMateria()};
                tabla.addRow(objeto);
            }
        } else {
            System.out.println("El objeto Alumno seleccionado o la lista de notas es nula.");
        }

        tablaNotas.setModel(tabla);
    }

    private void mostrarTablaAlumnos() {
        List<Nota> listaNotas = control.traerNotas();
        DefaultTableModel tabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        String titulos[] = {"Nombre", "Apellido", "Nota", "Materia"};
        tabla.setColumnIdentifiers(titulos);

        if (listaNotas != null) {
            for (Nota nota : listaNotas) {
                Object[] objeto = {nota.getAlumno().getNombre(), nota.getAlumno().getApellido(), nota.getValor(), nota.getMateria()};
                tabla.addRow(objeto);
            }
        } else {
            System.out.println("El objeto Alumno seleccionado o la lista de notas es nula.");
        }

        tablaNotas.setModel(tabla);
    }

